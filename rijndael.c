const unsigned char r_con[] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36, 0x6C,
};
void key_schedule_core(unsigned char *word, int iteration) {
    // Circular left shift
    unsigned char temp = word[0];
    word[0] = word[1];
    word[1] = word[2];
    word[2] = word[3];
    word[3] = temp;

    // SubBytes step
    for (int i = 0; i < 4; i++) {
        word[i] = s_box[word[i]];
    }

    // XOR with r_con
    word[0] ^= r_con[iteration];
}

